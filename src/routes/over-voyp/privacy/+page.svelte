	<h3 class="font-semibold uppercase">BESCHERMING GEGEVENS</h3>

	<p>
		Bij Voyp vinden wij privacy heel erg belangrijk en doen wij er alles aan om deze te waarborgen.
		Wij beschermen je gegevens alsof het onze eigen gegevens zijn. De beveiliging van jouw
		persoonsgegevens nemen wij zeer serieus. Als Verwerker dragen wij deze verantwoordelijkheid. Wij
		doen er dan ook alles aan om jouw persoonsgegevens te beschermen. Zo voorkomen wij dat er van
		buitenaf toegang tot de data mogelijk is en hebben medewerkers alleen toegang tot voor hen
		relevante data.
	</p>
	<p>
		Mocht er sprake zijn van een (potentieel) datalek, zoals opgenomen in artikel 33 van de AVG, dan
		brengen wij de Klant hier binnen 24 uur van op de hoogte. Indien wet en/of regelgeving dit
		vereist melden we een (eventueel) lek ook bij de autoriteiten. De Verwerkingsverantwoordelijke
		blijft in ieder geval de verantwoordelijke partij voor het voldoen aan de (wettelijke)
		meldplichten. In de melding melden we je de (vermeende) oorzaak van het lek, hoeveel bedrijven
		er (mogelijk) zijn benadeeld, wat de (te verwachten) gevolgen zijn, wat de (te verwachten)
		oplossing is en welke maatregelen er (eventueel) reeds genomen zijn. Uiteraard houden we je op
		de hoogte en melden we hoe we het lek hebben verholpen.
	</p>
	<p>
		Vermoed je dat de gegevens niet goed zijn beveiligd of heb je aanwijzingen van misbruik? Neem
		dan meteen contact op met onze klantenservice.
	</p>

	<h3 class="font-semibold uppercase">OPSLAG GEGEVENS</h3>

	<p>
		Wij slaan alleen gegevens van je op die nodig zijn om onze Diensten te leveren of onze
		Dienstverlening te verbeteren, doen dit nooit langer dan strikt noodzakelijk en leggen duidelijk
		uit wélke gegevens dit zijn en waarom die worden opgeslagen.
	</p>
	<p>
		Van websitebezoekers worden geen persoonsgegevens opgeslagen. Dus ook geen IP-adressen. Wel
		gebruiken we Google Analytics voor het analyseren van dataverkeer en conversies op onze website.
		Met Google is hiervoor een AVG-compliant verwerkersovereenkomst gesloten. Vanuit Analytics zijn
		de gegevens die worden opgeslagen niet herleidbaar tot een specifieke gebruiker.
	</p>
	<p>
		Indien een bezoeker zelf persoonsgegevens achterlaat, hetzij via de chat, hetzij via de bestel-
		of contactmodule, dan worden deze gegevens geautomatiseerd verwerkt in ons CRM systeem. Chats
		worden eveneens opgeslagen bij Tawk, onze chatleverancier. Deze chats en alle gegevens hiervan
		worden automatisch na 14 dagen bij Tawk verwijderd.
	</p>

	<p class="underline">Persoonsgegevens die kunnen worden opgeslagen</p>
	<div class="pl-2.5 md:pl-6">
		<p>– Bedrijfsnaam, adresgegevens bedrijf, telefoonnummer en mailadres</p>
		<p>– Naam, telefoonnummer en mailadres contactpersoon</p>
		<p>– Gegevens van bestelling, offerte en/of abonnementen</p>
		<p>– Bankrekeningnummer</p>
	</div>

	<p class="underline">Van Klanten worden de volgende persoonsgegevens aanvullend opgeslagen</p>
	<div class="pl-2.5 md:pl-6">
		<p>– Gespreksgeschiedenis van Klant (ontvangen/gevoerde gesprekken)</p>
		<p>– Tijdstippen en duur van de gesprekken</p>
		<p>– Indien van toepassing: gespreksopnames</p>
		<p>– IP-adressen</p>
	</div>

	<p>
		Deze gegevens zijn nodig om tot een Overeenkomst te komen en om de (Aanvullende) Diensten, zoals
		in de Overeenkomst met Klant overeengekomen, aan te kunnen bieden, te laten functioneren en te
		factureren.
	</p>
	<p>
		De gegevens worden ook gebruikt voor statistische doeleinden, zoals het inzien van conversies en
		het gebruik van (Aanvullende) Diensten. Ook indien er geen Overeenkomst tot stand is gekomen
		blijven deze gegevens daarom beschikbaar. Als laatste worden de gegevens gebruikt om Klanten te
		informeren over storingen, nieuwe diensten of andere belangrijke informatie.
	</p>
	<p>
		Alleen Voyp heeft toegang tot deze persoonsgegevens en verkoopt deze data nooit aan derden. Voyp
		verklaart nadrukkelijk zonder uitdrukkelijke schriftelijke en/of telefonische toestemming van
		Klant de gespreksgeschiedenis van Klant en eventueel daaraan in opdracht van Klant gekoppelde
		gespreksopnames nooit in te zien.
	</p>
	<p>
		Na beëindiging van de Overeenkomst verwijderd Voyp binnen 8 weken de aanvullende
		persoonsgegevens van Klanten. Eventueel kan Voyp aan de Klant na beëindiging van de Overeenkomst
		een kopie van deze data leveren alvorens de data te vernietigen. Indien Klant hier niet
		nadrukkelijk om heeft verzocht dan is Voyp gerechtigd om deze gegevens zonder nadere toestemming
		te vernietigen.
	</p>

	<h3 class="font-semibold uppercase">GEHEIMHOUDINGSPLICHT</h3>
	<p>
		Wij gebruiken je gegevens niet voor zaken anders dan omschreven in deze Verwerkersovereenkomst
		en verstrekken je gegevens nooit aan derden, tenzij dit nodig is om de Overeenkomst die we met
		onze Klanten hebben uit te voeren of om te voldoen aan wettelijke verplichtingen. Op alle
		persoonsgegevens die wij verzamelen en/of ontvangen verklaren we een geheimhoudingsplicht jegens
		derden van toepassing. De gegevens die wij hebben zullen dan ook nooit worden gebruikt voor
		andere zaken dan waarvoor ze strikt bedoeld zijn. Je persoonsgegevens worden dus nooit verkocht
		voor andere (marketings-)doeleinden.
	</p>
	<p>
		Wanneer wij wettelijk verplicht zijn om de persoonsgegevens te overhandigen is de
		geheimhoudingsplicht niet van toepassing. Evenmin is de geheimhoudingsplicht van toepassing bij
		nadrukkelijke toestemming van de Klant om gegevens te delen. Daarnaast is de
		geheimhoudingsplicht niet van toepassing indien het delen van de persoonsgegevens aan derden
		noodzakelijk is om de (Aanvullende) Diensten aan Klant te kunnen leveren.
	</p>
	<p>
		Uiteraard sluit Voyp met derden een Verwerkersovereenkomst die in de lijn ligt met de
		Verwerkersovereenkomst die we met jou als Klant hebben gesloten. Verwerkingsverantwoordelijke
		geeft Verwerker expliciet toestemming hiervoor. Verwerker geeft op verzoek van
		Verwerkersverantwoordelijke inzage in welke sub-verwerkers er zijn ingeschakeld.
	</p>

	<h3 class="font-semibold uppercase">VERANTWOORDELIJKHEID GEGEVENS</h3>
	<p>
		Voyp is alleen verantwoordelijk voor de persoonsgegevens die worden verzameld door Voyp op basis
		van deze Verwerkersovereenkomst. Uiteraard is Voyp nooit verantwoordelijk voor de verwerking van
		persoonsgegevens door andere partijen dan Voyp zelf. Klant verklaart dat de gegevens die aan
		Voyp worden verstrekt niet onrechtmatig zijn en geen inbreuk maken op de rechten van derden en
		vrijwaart Voyp van eventuele aanspraken.
	</p>
	<p>
		Je kunt als Klant Voyp altijd vragen om inzage in de opgeslagen gegevens en deze gegevens laten
		wijzigen, aanvullen of verwijderen. Je houdt dus zelf de zeggenschap over je persoonsgegevens.
		Zo blijf je als Klant altijd zelf eindverantwoordelijk over de persoonsgegevens die door Voyp
		worden verwerkt. Wij hebben géén zeggenschap over je persoonsgegevens, de verwerking hiervan
		vindt expliciet in opdracht van de Klant plaats.
	</p>
	<p>
		Inzicht in je gegevens kan door <a href="./contact" class="underline">contact</a> op te nemen met
		onze klantenservice. Wij reageren hier binnen 14 dagen op. Het kan zijn dat (bepaalde) persoonsgegevens
		niet kunnen worden verwijderd, omdat anders de Overeenkomst niet kan worden nagekomen of omdat er
		anders niet aan wettelijke verplichtingen wordt voldaan. Dit melden we je dan.
	</p>
	<p>
		Mocht een betrokkene zich bij Voyp melden met een vraag over (eventueel) bij Voyp opgeslagen
		persoonsgegevens, dan wordt dit verzoek doorgestuurd aan de Klant en helpen we je, voor zover
		mogelijk, met het afhandelen van dit verzoek. Het kan zijn dat Voyp hiervoor kosten in rekening
		dient te brengen bij Klant. Deze kondigen we vooraf aan.
	</p>

	<h3 class="font-semibold uppercase">AUDIT</h3>
	<p>
		Je hebt als Verwerkingsverantwoordelijke het recht de afspraken uit deze Verwerkersovereenkomst
		te laten toetsen (audit) door een onafhankelijke deskundige derde. Dit kan alleen nadat je bij
		ons (de Verwerker) relevante rapportages hebt opgevraagd en wanneer deze rapportages niet of
		niet-voldoende inzichtelijk maken dat de afspraken uit deze Verwerkersovereenkomst worden
		nageleefd. Deze audit vindt plaats niet eerder dan 4 weken na de aankondiging en maximaal 1x per
		kalenderjaar. Voyp werkt vanzelfsprekend mee aan deze audit en zal alle relevante en benodigde
		informatie ter beschikking stellen. De kosten hiervan, inclusief de kosten die wij als Verwerker
		maken, komen voor rekening van de Verwerkersverantwoordelijke.
	</p>

	<h3 class="font-semibold uppercase">GESCHILLEN</h3>
	<p>
		Bij een geschil kun je je tot ons wenden of tot de Autoriteit Persoonsgegevens. Voyp werkt,
		indien wettelijk verplicht en noodzakelijk, altijd mee aan een eventueel onderzoek naar een
		verwerking op basis van deze Verwerkersovereenkomst. Eventuele kosten worden in rekening
		gebracht bij de Klant. Indien het komt tot een gerechtelijke procedure dan kiest Voyp domicilie
		in Amsterdam.
	</p>

